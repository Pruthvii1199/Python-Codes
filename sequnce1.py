# sequence type
# string
# string can be written in 6 ways ('abc'),("abc"),(''abc''),(""abc""),('''abc'''),("""abc""")
# STANDARD format is single quote ('')
# way 1 
pair1 = 'GBPUSD'
print(pair1)
print(type(pair1))

# way 2
pair2 = "EUROUSD"
print(pair2)
print(type(pair2))

# way 3
pair3 = '''USDJPY'''
print(pair3)
print(type(pair3))